DATA	SEGMENT
LED		DB 3FH, 06H, 5BH, 4FH, 66H, 6DH, 7DH, 07H
		DB 7FH, 67H, 77H, 7CH, 39H, 5EH, 79H, 71H
DATA	ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:	MOV AX, DATA
		MOV DS, AX		; INITIALIZE DS
		MOV DX, 0E48BH
		MOV AL, 89H
		OUT DX, AL
INOUT:	MOV DX, 0E48AH	; WRITE CONTROL WORD
		IN AL, DX
		AND AL, 0FH		; PRESERVE LOWER 4 BITS
		MOV BX, OFFSET LED
		XLAT			; TABLE LOOKUP
		MOV DX, 0E488H
		OUT DX, AL		; OUT TO DISPLAY
		MOV AH, 1
		INT 16H
		JZ INOUT		; BACK IF NOT PRESSED
		MOV AH, 4CH
		INT 21H			; EXIT
CODE ENDS
END START
