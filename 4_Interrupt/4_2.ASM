CODE SEGMENT
	ASSUME CS:CODE
START:
	; CHANNEL 0: COUNT=200, CHANNEL 1: COUNT=10000
	MOV AL, 00010110B	; CONFIGURE CHANNEL 0
	MOV DX, 0E483H
	OUT DX, AL
	MOV AL, 200			; SET CHANNEL 0 COUNTER
	MOV DX, 0E480H
	OUT DX, AL
	MOV AL, 01110110B	; CONFIGURE CHANNEL 1
	MOV DX, 0E483H
	OUT DX, AL
	MOV AX, 10000		; SET CHANNEL 1 COUNTER
	MOV DX, 0E481H
	OUT DX, AL			; LOWER 8 BIT
	MOV DX, 0E481H
	MOV AL, AH
	OUT DX, AL			; HIGHER 8 BIT
LOP:
	MOV AH, 1
	INT 16H				; CHECK FOR KEYBOARD INTERRUPT
	JZ LOP
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START
