DATA	SEGMENT
LED		DB 3FH, 06H, 5BH, 4FH, 66H, 6DH, 7DH, 07H
		DB 7FH, 67H, 77H, 7CH, 39H, 5EH, 79H, 71H
CNT		DB 00H	
DATA 	ENDS

CODE	SEGMENT
	ASSUME CS:CODE, DS:DATA
START:	MOV AX, DATA
		MOV DS, AX		; INIT DATA SEGMENT
		MOV DX, 0E48BH
		MOV AL, 10000000B
		OUT DX, AL		; SET A AND C OUT
INOUT:	MOV BX, OFFSET LED	; SET TABLE ADDRESS
		MOV AL, 01H
		MOV DX, 0E488H
		OUT DX, AL		; ENABLE DIGITRON 0
		MOV CNT, 00H	; AH STORE CURRENT NUMBER
LOOP0:	MOV AL, CNT
		XLAT			; TABLE LOOKUP
		MOV DX, 0E48AH
		OUT DX, AL		; PRINT TO DIGITRON
		MOV AH, 1
		INT 16H
		JNZ FIN
		MOV AH, 86H
		MOV CX, 5
		MOV DX, 0
		INT 15H
		INC CNT
		CMP CNT, 15
		JG OUT0			; IF AH > 15 THEN OUT
		JMP LOOP0
OUT0:	MOV DX, 0E488H
		MOV AL, 02H
		OUT DX, AL		; ENABLE DIGITRON 1
		MOV CNT, 00H
LOOP1: 	MOV AL, CNT
		XLAT
		MOV DX, 0E48AH
		OUT DX, AL		; PIRNT TO DIGITRON
		MOV AH, 1
		INT 16H
		JNZ FIN
		MOV AH, 86H
		MOV CX, 5
		MOV DX, 0
		INT 15H
		INC CNT
		CMP CNT, 15
		JG OUT1
		JMP LOOP1
OUT1:	JMP INOUT
FIN:	MOV AH, 4CH
		INT 21H
CODE ENDS
END START
