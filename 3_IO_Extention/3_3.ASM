DATA	SEGMENT
DATA	ENDS

CODE 	SEGMENT
	ASSUME CS:CODE, DS:DATA
START:	MOV AX, DATA
		MOV DS, AX		; INIT DATA SEGMENT REGISTER
		MOV DX, 0E48BH
		MOV AL, 10001001B
		OUT DX, AL		; SET A OUT C IN

INOUT:	MOV DX, 0E48AH
		IN AL, DX		; GET INPUT FROM C
		MOV AH, AL
		AND AL, 0FH		; AL STORES C LOWER 4 BITS
		MOV CL, 4
		SHR AH, CL		; AH STORES C HIGHER 4 BITS
		ADD AL, AH
		DAA				; BCD ADJUST
		MOV DX, 0E488H
		OUT DX, AL		; OUTPUT AL TO PORT A
		MOV AH, 1
		INT 16H
		JZ INOUT
		MOV AH, 4CH
		INT 21H
CODE ENDS
END START
