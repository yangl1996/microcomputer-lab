DATA SEGMENT
        SUMLOW DB ?
        SUMHIGH DB ?
        COUNT DB 0AH
        ADDER DB 30H, 30H, 30H, 30H, 30H, 30H, 30H, 30H, 30H, 30H
DATA ENDS

CODE SEGMENT
        ASSUME CS:CODE, DS:DATA
START:  MOV AX, DATA
        MOV DS, AX      ; SET UP DATA SEGMENT
        MOV AL, [COUNT]   ; AL HOLDS COUNTER
        MOV CL, 0H      ; CL HOLDS CURRENT SUM LOWER
        MOV CH, 0H      ; CH HOLDS CURRENT SUM HIGHER

LOOP1:  MOV AH, 0H      ; CLEAR AL
        MOV BL, AL      ; BACKUP AL
        SUB BL, 0H      ; CHECK WHETHER COUNT IS ZERO
        JE EXIT         ; IF COUNTER IS 0 THEN EXIT
        MOV SI, AL
        SUB SI, 1H
        ADD CL, ADDER[SI]
        ADC CH, 0H      ; HANDLE OVERFLOW
        SUB AL, 1H      ; DECREASE COUNTER
        JMP LOOP1       ; LOOP

EXIT:   MOV SUMLOW, CL
        MOV SUMHIGH, CH
        MOV AX, 4C00H 
        INT 21H
CODE ENDS
END START
